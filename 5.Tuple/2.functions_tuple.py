# len(), max(), min(), sum()
tup1 = (1, 2, 3, 4, "num", 10.5, True)  # (1, 2, 3, 4, 'num', 10.5, True)
print(len(tup1))  # 7
# print(max(tup1)) #'>' not supported between instances of 'str' and 'int'
tup2 = 1, 2, 3, 5, 4, 3, 33, 333, 55754
print(max(tup2))  # 55754
print(min(tup2))  # 1
print(sum(tup2))  # 56138
