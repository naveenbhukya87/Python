list = ["govi", 122, 10.3, True, "True"]
print(list)
list[0] = "govind"
print(list)
# ['govi', 122, 10.3, True, 'True']
# ['govind', 122, 10.3, True, 'True']
# To change 2 or more elements:
list[0:2] = "govind", "100"
print(list)
# ['govind', '100', 10.3, True, 'True'] and data types can also be different i.e "govind" can be changed with 100
