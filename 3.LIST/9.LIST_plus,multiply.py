# + is used for addition
list1 = [1, 3, 5, 6, 10]
list2 = [2, 4, 7, 9]
list3 = list1+list2
print(list1+list2)  # [1, 3, 5, 6, 10, 2, 4, 7, 9]
print(list3)  # [1, 3, 5, 6, 10, 2, 4, 7, 9]
# [1, 3, 5, 6, 10, 2, 4, 7, 9, 1, 3, 5, 6, 10, 2, 4, 7, 9, 1, 3, 5, 6, 10, 2, 4, 7, 9]
print(list3*3)
# [1, 3, 5, 6, 10, 2, 4, 7, 9, 2, 4, 7, 9, 2, 4, 7, 9] == list1+(list2*3)
print(list1+list2*3)
# [1, 3, 5, 6, 10, 1, 3, 5, 6, 10, 1, 3, 5, 6, 10, 2, 4, 7, 9, 2, 4, 7, 9, 2, 4, 7, 9]==(list1*3)+list2
print(list1*3+list2*3)
# [1, 3, 5, 6, 10, 2, 4, 7, 9, 1, 3, 5, 6, 10, 2, 4, 7, 9, 1, 3, 5, 6, 10, 2, 4, 7, 9]==(list1+list2)*3
print((list1+list2)*3)
# [1, 3, 5, 6, 10, 2, 4, 7, 9, 1, 3, 5, 6, 10, 2, 4, 7, 9, 1, 3, 5, 6, 10, 2, 4, 7, 9]==(list1+list2)*3
print(3*(list1+list2))
