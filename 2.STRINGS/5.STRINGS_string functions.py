# len() function: returns length of the string
# max() function: returns character having maximum ASCII Value
# min() function: returns character having minimum ASCII Value
print(len("govind"))  # 6 (6 characters are there)
print(max("govind"))  # v
print(max("GOVINd"))  # d
print(max("g0^!/\/|)@#$%^&*()_+{}:"))  # }
print(min("govind"))  # d
print(min("GOVINd"))  # G
print(min("g0^!/\/|)@#$%^&*()_+{}:"))  # !
print(len("g0^!/\/|)@#$%^&*()_+{}:"))  # 23
