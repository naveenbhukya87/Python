#'in' operator checks for existence of a given string in another string and
    # if exists returns "True"
    #if doesn't exists, returns "False"
#'not in' operator checks for existence of a given string in another string and
    #if exists returns "False"
    #if doesn't exists, returns "True"
name="govind"
print("ind" in name) #OUTPUT: True
print("ind" not in name) #OUTPUT: False
print("win" in name) #OUTPUT: False
print("win" not in name) #OUTPUT: True
print("to p" in "Expacto patronum") #True